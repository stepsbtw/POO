public class Data {
    private int dia, mes, ano;
    void setDia(int x){
        dia = x;
    }
    void setMes(int x){
        mes = x;
    }
    void setAno(int x){
        ano = x;
    }
    int getDia(){
        return dia;
    }
    int getMes(){
        return mes;
    }
    int getAno(){
        return ano;
    }
}
